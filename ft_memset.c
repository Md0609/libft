#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char		*ntr;
	size_t		i;

	i = 0;
	ntr = (char *)s;
	while (i < n)
	{
		ntr[i] = (char)c;
		i++;
	}
	return (s);
}
